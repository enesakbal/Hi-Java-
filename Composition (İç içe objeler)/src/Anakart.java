
public class Anakart {
    
    
    private String anakart_modeli;
    private String anakart_markasi;
    private int anakart_fiyati;

    public Anakart(String anakart_modeli, String anakart_markasi, int anakart_fiyati) {
        this.anakart_modeli = anakart_modeli;
        this.anakart_markasi = anakart_markasi;
        this.anakart_fiyati = anakart_fiyati;
    }

    public String getAnakart_modeli() {
        return anakart_modeli;
    }

    public void setAnakart_modeli(String anakart_modeli) {
        this.anakart_modeli = anakart_modeli;
    }

    public String getAnakart_markasi() {
        return anakart_markasi;
    }

    public void setAnakart_markasi(String anakart_markasi) {
        this.anakart_markasi = anakart_markasi;
    }

    public int getAnakart_fiyati() {
        return anakart_fiyati;
    }

    public void setAnakart_fiyati(int anakart_fiyati) {
        this.anakart_fiyati = anakart_fiyati;
    }
    
    
    
    
    
    
    
    
    
}
